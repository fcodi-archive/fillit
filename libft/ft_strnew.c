#include "includes/libft.h"

char	*ft_strnew(size_t size)
{
	char	*string;

	if (size == (size_t)-1)
		return (NULL);
	if ((string = (char *)ft_memalloc(++size)))
		ft_bzero(string, size);
	return (string);
}
